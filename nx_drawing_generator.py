def main():
    for f in (DWG, PDF, REPORT):
        if os.path.exists(f):
            os.remove(f)

    mpart, _ = s.Parts.OpenDisplay(MODEL)
    metric = mpart.PartUnits == NXOpen.BasePart.Units.Millimeters
    rep("Model: %s (%s)" % (mpart.Leaf, "mm" if metric else "inch"))

    if metric:
        template, pres, units = ("Drawing-A3-Size-template.prt", "A3 - Size",
                                 NXOpen.Part.Units.Millimeters)
    else:
        template, pres, units = ("Drawing-B-Size-template.prt", "B - Size",
                                 NXOpen.Part.Units.Inches)
    fn = s.Parts.FileNew()
    fn.TemplateFileName = template
    fn.UseBlankTemplate = False
    fn.ApplicationName = "DrawingTemplate"
    fn.Units = units
    fn.TemplateType = NXOpen.FileNewTemplateType.Item
    fn.TemplatePresentationName = pres
    fn.UsesMasterModel = "Yes"
    fn.MasterFileName = MODEL
    fn.NewFileName = DWG
    fn.MakeDisplayedPart = True
    fn.Commit()
    fn.Destroy()
    wp = s.Parts.Work

    master = None
    for p in s.Parts:
        if os.path.normcase(p.FullPath) == os.path.normcase(MODEL):
            master = p
    if master is None:
        raise RuntimeError("master model not loaded")

    # refset + hide template datums
    try:
        names = [r.Name for r in master.GetAllReferenceSets()]
        target = next((n for n in names if n.upper() in ("MODEL", "SOLID", "BODY")), None)
        if target:
            for comp in wp.ComponentAssembly.RootComponent.GetChildren():
                wp.ComponentAssembly.ReplaceReferenceSet(comp, target)
    except Exception:
        pass
    try:
        blank = []
        for cn in ("Datums", "Points", "CoordinateSystems"):
            coll = getattr(wp, cn, None)
            if coll is not None:
                blank.extend(list(coll))
        if blank:
            s.DisplayManager.BlankObjects(blank)
    except Exception:
        pass

    # 1-2: analyze + plan
    geom = Geometry(master)
    holes = group_holes(geom)
    rep("Geometry: extent %.5g x %.5g x %.5g; levels X:%d Y:%d Z:%d; %d hole feature(s)"
        % (tuple(geom.ext) + tuple(len(l) for l in geom.levels) + (len(holes),)))
    if getattr(geom, "n_circ_fail", 0):
        rep("NOTE: %d of %d circular edges could not be fitted (non-axis-aligned?)"
            % (geom.n_circ_fail, geom.n_circ))
    if os.environ.get("NXDRAW_DEBUG"):
        for axis, coord, area in sorted(geom.planar_faces, key=lambda t: -t[2])[:10]:
            say("  face: %s=%.5g area %.6g" % ("XYZ"[axis], coord, area))
    specs = plan_dimensions(geom, holes, metric)

    w_mm = wp.PartUnits == NXOpen.BasePart.Units.Millimeters
    u = 1.0 if metric == w_mm else (1 / 25.4 if metric else 25.4)
    P = 1.0 if w_mm else 1 / 25.4       # paper unit (annotation mm) in drawing units

    sheet = wp.DrawingSheets.CurrentDrawingSheet
    field, obstacles = detect_field(wp, sheet, template)

    # reserve space for the general tolerance note above the title block
    kos = TEMPLATE_KEEPOUTS.get(template, [])
    note_rect = None
    if kos:
        tb = max((Rect(*k) for k in kos),
                 key=lambda r: (r.x1 - r.x0) * (r.y1 - r.y0))
        note_rect = Rect(tb.x0, tb.y1 + 2 * P, tb.x0 + 100 * P, tb.y1 + 26 * P)
        obstacles.append(note_rect)

    # 3-4: layout
    lay = solve_layout(geom, specs, field, obstacles, u, P)
    num, den, sc = lay["num"], lay["den"], lay["sc"]

    dsb = wp.DrawingSheets.DrawingSheetBuilder(sheet)
    try:
        dsb.Option = NXOpen.Drawings.DrawingSheetBuilder.SheetOption.StandardSize
        dsb.ProjectionAngle = NXOpen.Drawings.DrawingSheetBuilder.SheetProjectionAngle.Third
        dsb.Height, dsb.Length = sheet.Height, sheet.Length
        want = "S%d%d" % (num, den)
        if metric:
            dsb.Units = NXOpen.Drawings.DrawingSheetBuilder.SheetUnits.Metric
            et = NXOpen.Drawings.DrawingSheetBuilder.SheetStandardMetricScale
            dsb.StandardMetricScale = getattr(et, want) if hasattr(et, want) else et.Custom
        else:
            dsb.Units = NXOpen.Drawings.DrawingSheetBuilder.SheetUnits.English
            et = NXOpen.Drawings.DrawingSheetBuilder.SheetStandardEnglishScale
            dsb.StandardEnglishScale = getattr(et, want) if hasattr(et, want) else et.Custom
        if not hasattr(et, want):
            dsb.ScaleNumerator, dsb.ScaleDenominator = float(num), float(den)
        dsb.Number = "1"
        dsb.Commit()
    finally:
        dsb.Destroy()
    sheet = wp.DrawingSheets.CurrentDrawingSheet

    views = {}

    def add_view(model_view, x, y, vnum, vden, dashed=True, key=None):
        bvb = wp.DraftingViews.CreateBaseViewBuilder(NXOpen.Drawings.BaseView.Null)
        try:
            bvb.SelectModelView.SelectedView = wp.ModelingViews.FindObject(model_view)
            bvb.Scale.Numerator = float(vnum)
            bvb.Scale.Denominator = float(vden)
            if dashed:
                # hidden lines dashed in orthographic views (ASME Y14.2 line
                # conventions); pictorial views omit them for clarity (Y14.3)
                hl = bvb.Style.ViewStyleHiddenLines
                if isinstance(hl.HiddenLine, bool):
                    hl.HiddenLine = True
                hl.Font = NXOpen.Preferences.Font.Dashed
            bvb.Placement.Placement.SetValue(None, None, NXOpen.Point3d(x, y, 0.0))
            views[key or model_view] = bvb.Commit()
            say("View %s at (%.1f, %.1f)" % (model_view, x, y))
        finally:
            bvb.Destroy()

    for name in ("Front", "Top", "Right"):
        x, y = lay["views"][name]
        add_view(name, x, y, num, den)
    ix, iy, shrink = lay["iso"]
    inum, iden = num, den
    if shrink < 1.0:
        inum, iden = num, den * (2 if shrink <= 0.5 else 1)
        if shrink == 0.75:
            inum, iden = num * 3, den * 4
    add_view("Trimetric", ix, iy, inum, iden, dashed=False, key="Iso")

    # 5: dimensions
    comp0 = wp.ComponentAssembly.RootComponent.GetChildren()[0]

    def occ(obj):
        try:
            o = comp0.FindOccurrence(obj)
            return o if o is not None else obj
        except Exception:
            return obj

    am = wp.Annotations
    po = NXOpen.Annotations.AssociativityPointOption

    def assoc(obj, view, pt, opt):
        a = am.NewAssociativity()
        a.FirstObject = occ(obj)
        a.ObjectView = view
        a.PickPoint = pt
        a.PointOption = opt
        return a

    def model_to_sheet(vname, pt3):
        """Model point -> sheet coords in the given principal view."""
        h_ax, v_ax, _ = VIEWDEF[vname]
        cx, cy = lay["views"][vname]
        mid = [(geom.lo[i] + geom.hi[i]) / 2 for i in range(3)]
        p = (pt3[0], pt3[1], pt3[2])
        return (cx + (p[h_ax] - mid[h_ax]) * sc, cy + (p[v_ax] - mid[v_ax]) * sc)

    def dim_origin(sp, pa=None, pb=None):
        """Text origin: dim band offset on the side axis, mid-span of the
        measured points along the other axis (text sits between its arrows)."""
        vr = lay["vrects"][sp.view_name]
        off = (12.0 + 10.0 * sp.row) * P
        mid_x = (vr.x0 + vr.x1) / 2
        mid_y = (vr.y0 + vr.y1) / 2
        if pa is not None and pb is not None:
            sa = model_to_sheet(sp.view_name, pa)
            sb = model_to_sheet(sp.view_name, pb)
            mid_x = (sa[0] + sb[0]) / 2
            mid_y = (sa[1] + sb[1]) / 2
        if sp.side == "below":
            return (mid_x, vr.y0 - off)
        if sp.side == "above":
            return (mid_x, vr.y1 + off)
        if sp.side == "left":
            return (vr.x0 - off, mid_y)
        return (vr.x1 + off, mid_y)

    def set_unidirectional(dim):
        """ASME Y14.5 unidirectional system: all dimension text reads
        horizontally from the bottom of the sheet (no leader-aligned or
        rotated text)."""
        try:
            dp = dim.GetDimensionPreferences()
            dp.TextOrientation = NXOpen.Annotations.TextOrientation.Horizontal
            dim.SetDimensionPreferences(dp)
        except Exception:
            pass

    def narrow_origin(sp, ox, oy, pa, pb):
        """Arrows + text cannot fit between close extension lines (~18mm on
        paper): move the text outside the span along the dimension line
        (ASME narrow-dimension practice), dodging other text in the band."""
        if sp.span * sc >= 18 * P:
            return ox, oy
        sa = model_to_sheet(sp.view_name, pa)
        sb = model_to_sheet(sp.view_name, pb)
        placed = text_pos.get((sp.view_name, sp.side), [])
        if sp.horizontal:
            ox = max(sa[0], sb[0]) + 9 * P
            guard = 0
            while guard < 12 and any(abs(ox - t) < 8 * P for t in placed):
                ox += 8 * P
                guard += 1
        else:
            oy = max(sa[1], sb[1]) + 9 * P
            guard = 0
            while guard < 12 and any(abs(oy - t) < 8 * P for t in placed):
                oy += 8 * P
                guard += 1
        return ox, oy

    made = 0
    callout_x = {}      # view_name -> placed callout x positions
    datum_origins = {}  # letter -> sheet origin of the datum symbol
    text_pos = {}       # (view, side) -> along-axis coords of placed dim text
    for sp in specs:
        if getattr(sp, "skip", True):
            continue
        view = views[sp.view_name]
        try:
            if sp.kind == "linear":
                e0, p0, e1, p1 = sp.geo
                dd = am.NewDimensionData()
                dd.SetAssociativity(1, [assoc(e0, view, p0, po.OnCurve)])
                dd.SetAssociativity(2, [assoc(e1, view, p1, po.OnCurve)])
                ox, oy = dim_origin(sp, (p0.X, p0.Y, p0.Z), (p1.X, p1.Y, p1.Z))
                ox, oy = narrow_origin(sp, ox, oy,
                                       (p0.X, p0.Y, p0.Z), (p1.X, p1.Y, p1.Z))
                text_pos.setdefault((sp.view_name, sp.side), []).append(
                    ox if sp.side in ("below", "above") else oy)
                opt = NXOpen.Point3d(ox, oy, 0.0)
                if sp.horizontal:
                    d = wp.Dimensions.CreateHorizontalDimension(dd, opt)
                else:
                    d = wp.Dimensions.CreateVerticalDimension(dd, opt)
                set_unidirectional(d)
            elif sp.kind == "pos":
                h = sp.hole
                e0, p0 = sp.geo
                cpt = NXOpen.Point3d(*h["center"])
                dd = am.NewDimensionData()
                dd.SetAssociativity(1, [assoc(e0, view, p0, po.OnCurve)])
                dd.SetAssociativity(2, [assoc(h["edge"], view, cpt, po.ArcCenter)])
                ox, oy = dim_origin(sp, (p0.X, p0.Y, p0.Z), h["center"])
                ox, oy = narrow_origin(sp, ox, oy,
                                       (p0.X, p0.Y, p0.Z), h["center"])
                text_pos.setdefault((sp.view_name, sp.side), []).append(
                    ox if sp.side in ("below", "above") else oy)
                opt = NXOpen.Point3d(ox, oy, 0.0)
                if sp.horizontal:
                    d = wp.Dimensions.CreateHorizontalDimension(dd, opt)
                else:
                    d = wp.Dimensions.CreateVerticalDimension(dd, opt)
                # locations controlled by position tolerances are BASIC dims
                # (ASME Y14.5 para 8.4 — untoleranced theoretically exact)
                try:
                    tt = getattr(NXOpen.Annotations, "ToleranceType", None)
                    if tt is not None and hasattr(tt, "Basic"):
                        d.ToleranceType = tt.Basic
                except Exception:
                    sp.why = "placed, but basic style not applied"
                set_unidirectional(d)
            elif sp.kind == "dia":
                h = sp.hole
                h_ax, v_ax, _ = VIEWDEF[sp.view_name]
                vr = lay["vrects"][sp.view_name]
                band = lay["res"](sp.view_name, "above")
                hx = model_to_sheet(sp.view_name, h["center"])[0]
                # text offset to the right of the hole's own extension line;
                # dodge other holes' extension lines and other callouts
                sx = hx + 16 * P
                ext_xs = [model_to_sheet(sp.view_name, h2["center"])[0]
                          for h2 in holes if HOLE_VIEW[h2["axis"]] == sp.view_name]
                # spacing must clear a whole neighbor group (frame ~44 wide)
                # plus this callout's leader corridor, so the leader cannot
                # pass through a neighbor's tolerance frame
                used = callout_x.setdefault(sp.view_name, [])
                guard = 0
                while guard < 40 and (
                        any(abs(sx - x0) < 58 * P for x0 in used)
                        or any(abs(sx - x0) < 8 * P for x0 in ext_xs)):
                    sx += 10 * P
                    guard += 1
                used.append(sx)
                # attach on the circle quadrant facing the callout
                dxs = 1.0 if sx >= hx else -1.0
                pk = list(h["center"])
                pk[h_ax] += dxs * h["r"] * 0.7071
                pk[v_ax] += h["r"] * 0.7071
                rdb = wp.Dimensions.CreateRadialDimensionBuilder(None)
                try:
                    try:
                        rdb.IsHoleStyle = True
                    except Exception:
                        pass
                    rdb.FirstAssociativity.SetValue(occ(h["edge"]), view,
                                                    NXOpen.Point3d(*pk))
                    # hole extent on the callout (ASME Y14.5 hole callouts:
                    # depth for blind holes, THRU when it pierces the part;
                    # words used in lieu of the depth symbol, per pre-1994
                    # practice, still unambiguous)
                    try:
                        note = "THRU" if h["through"] else (
                            "%.5g DEEP" % h["span"] if h["span"] > 0 else None)
                        if note:
                            atb = rdb.AppendedText
                            for setter in ("SetBelow", "SetAfter"):
                                if hasattr(atb, setter):
                                    getattr(atb, setter)([note])
                                    break
                    except Exception:
                        say("  callout note failed:\n"
                            + traceback.format_exc().splitlines()[-1])
                    oy = vr.y1 + band + 24 * P
                    h["callout_origin"] = (sx, oy)
                    rdb.Origin.Origin.SetValue(None, None, NXOpen.Point3d(sx, oy, 0.0))
                    h["dim_obj"] = rdb.Commit()
                    set_unidirectional(h["dim_obj"])
                finally:
                    rdb.Destroy()
            elif sp.kind == "datum":
                e, pt = sp.geo
                vs = e.GetVertices()
                a2, b2 = vs[0], vs[1]
                placed = text_pos.get((sp.view_name, sp.side), [])
                if placed:
                    # band shared with dims: attach near the edge end at the
                    # low corner — the leader then approaches outside the dim
                    # lines' span (Y14.5: leaders must not cross dim lines;
                    # crossing extension line ends is permitted)
                    sa = model_to_sheet(sp.view_name, (a2.X, a2.Y, a2.Z))
                    sb = model_to_sheet(sp.view_name, (b2.X, b2.Y, b2.Z))
                    ci = 1 if sp.side in ("left", "right") else 0
                    frac = 0.10 if sa[ci] <= sb[ci] else 0.90
                    best_att = (a2.X + frac * (b2.X - a2.X),
                                a2.Y + frac * (b2.Y - a2.Y),
                                a2.Z + frac * (b2.Z - a2.Z))
                else:
                    # empty band: attach where farthest from any dim text
                    best_att, best_d = None, -1.0
                    for frac in (0.85, 0.15, 0.65, 0.35, 0.5):
                        cand = (a2.X + frac * (b2.X - a2.X),
                                a2.Y + frac * (b2.Y - a2.Y),
                                a2.Z + frac * (b2.Z - a2.Z))
                        sxy = model_to_sheet(sp.view_name, cand)
                        coord = sxy[0] if sp.side in ("below", "above") else sxy[1]
                        d = min((abs(coord - t) for t in placed), default=1e9)
                        if d > best_d:
                            best_d, best_att = d, cand
                att = NXOpen.Point3d(*best_att)
                sp.att = att
                dsb = am.Datums.CreateDraftingDatumFeatureSymbolBuilder(
                    NXOpen.Annotations.Datum.Null)
                try:
                    dsb.Letter = sp.letter
                    ld = am.CreateLeaderData()
                    ld.Leader.SetValue(occ(e), view, att)
                    dsb.Leader.Leaders.Append(ld)
                    ox, oy = dim_origin(sp, (att.X, att.Y, att.Z), (att.X, att.Y, att.Z))
                    # extra clearance beyond the dim rows on this band
                    if sp.side == "below":
                        oy -= 5 * P
                    elif sp.side == "above":
                        oy += 5 * P
                    elif sp.side == "left":
                        ox -= 5 * P
                    else:
                        ox += 5 * P
                    # band shared with dims: escape past the low view corner
                    # (matches the end-attach above; leader clears dim lines)
                    if placed:
                        vr2 = lay["vrects"][sp.view_name]
                        if sp.side in ("left", "right"):
                            oy = vr2.y0 - 10 * P
                        else:
                            ox = vr2.x0 - 10 * P
                    fld = lay["field"]
                    ox = min(max(ox, fld.x0 + 16 * P), fld.x1 - 16 * P)
                    oy = min(max(oy, fld.y0 + 8 * P), fld.y1 - 8 * P)
                    dsb.Origin.Origin.SetValue(None, None, NXOpen.Point3d(ox, oy, 0.0))
                    dsb.Commit()
                    datum_origins[sp.letter] = (ox, oy)
                finally:
                    dsb.Destroy()
            elif sp.kind in ("fcf", "fcf_qual"):
                ech = NXOpen.Annotations.FeatureControlFrameBuilder.FcfCharacteristic
                fcf = am.CreateDraftingFeatureControlFrameBuilder(
                    NXOpen.Annotations.Gdt.Null)
                try:
                    fcf.Characteristic = getattr(ech, sp.characteristic)
                    fd = fcf.FeatureControlFrameDataList.FindItem(0)
                    fd.ToleranceValue = sp.tolval
                    # datum references (position: |prim|sec|tert;
                    # perpendicularity: |A or |A|B; flatness: none)
                    refs = (fd.PrimaryDatumReference,
                            fd.SecondaryDatumReference,
                            fd.TertiaryDatumReference if
                            hasattr(fd, "TertiaryDatumReference") else None)
                    for ref, letter in zip(refs, sp.datums):
                        if ref is None:
                            break
                        if hasattr(ref, "Letter"):
                            ref.Letter = letter
                        else:
                            dump("DatumReferenceBuilder", ref)
                            raise RuntimeError("no Letter on datum ref")
                    if sp.kind == "fcf":
                        # diameter zone symbol for position tolerance
                        for a in dir(fd):
                            if "zoneshape" in a.lower() or a == "ZoneShape":
                                try:
                                    zt = type(getattr(fd, a))
                                    for m in dir(zt):
                                        if "diameter" in m.lower():
                                            setattr(fd, a, getattr(zt, m))
                                            break
                                except Exception:
                                    pass
                                break
                        # leaderless frame docked directly under the diameter
                        # callout text — reads as one group per Y14.5, and a
                        # second leader to the hole would cross dims/views
                        h = sp.hole
                        cx0, cy0 = h.get("callout_origin", (None, None))
                        vr = lay["vrects"][sp.view_name]
                        if cx0 is None:
                            cx0, cy0 = vr.x1 + 20 * P, vr.y1 + 10 * P
                        # below the two-line callout text (size + THRU/DEEP),
                        # shifted right so the leader stub clears the frame
                        fcf.Origin.Origin.SetValue(
                            None, None, NXOpen.Point3d(cx0 + 4 * P, cy0 - 16 * P, 0.0))
                    else:
                        # qualification frame stacked directly under its datum
                        # symbol, leader to the same attach point (one group)
                        asp = next(s2 for s2 in specs
                                   if s2.kind == "datum"
                                   and getattr(s2, "letter", "") == sp.under)
                        e, pt = asp.geo
                        pt = getattr(asp, "att", pt)
                        ax0, ay0 = datum_origins.get(sp.under, (None, None))
                        vr = lay["vrects"][sp.view_name]
                        if ax0 is None:
                            ax0, ay0 = (vr.x0 + vr.x1) / 2, vr.y0 - 24 * P
                        # frame beside/below the datum box (combined group,
                        # no extra band row); dodges obstacles and the
                        # adjacent band's dimension rows
                        fld = lay["field"]
                        vr = lay["vrects"][sp.view_name]
                        if asp.side in ("below", "above"):
                            cands = [(ax0 + 15 * P, ay0), (ax0 - 42 * P, ay0),
                                     (ax0, ay0 - 12 * P)]
                        else:
                            bel = lay["res"](sp.view_name, "below")
                            qy_deep = min(ay0, vr.y0 - bel) - 12 * P
                            if text_pos.get((asp.view_name, asp.side)):
                                # corner-escaped datum sits near the below
                                # band: drop the frame beneath those rows
                                cands = [(ax0, qy_deep), (ax0 - 42 * P, qy_deep)]
                            else:
                                cands = [(ax0, ay0 - 12 * P), (ax0, qy_deep)]
                        qx, qy = cands[-1]
                        for cx2, cy2 in cands:
                            r2 = Rect(cx2 - 2 * P, cy2 - 5 * P,
                                      cx2 + 28 * P, cy2 + 5 * P)
                            if r2.x0 < fld.x0 or r2.x1 > fld.x1 or r2.y0 < fld.y0:
                                continue
                            if any(r2.overlaps(o, pad=1.5 * P)
                                   for o in lay["obstacles"]):
                                continue
                            if any(r2.overlaps(v2, pad=1.5 * P)
                                   for v2 in lay["vrects"].values()):
                                continue
                            qx, qy = cx2, cy2
                            break
                        # adjacent to its datum symbol: adjacency conveys the
                        # association, and a second leader to the same attach
                        # point would cross the datum box (leaders must not
                        # cross annotation per Y14.5 practice)
                        adjacent = abs(qx - ax0) <= 20 * P and abs(qy - ay0) <= 16 * P
                        if not adjacent:
                            # attach diverged along the edge from the datum's
                            # own attach so this leader clears the datum box
                            att2 = pt
                            try:
                                vs2 = e.GetVertices()
                                a3, b3 = vs2[0], vs2[1]
                                dx3 = b3.X - a3.X
                                dy3 = b3.Y - a3.Y
                                dz3 = b3.Z - a3.Z
                                ln2 = dx3 * dx3 + dy3 * dy3 + dz3 * dz3
                                t0 = ((pt.X - a3.X) * dx3 + (pt.Y - a3.Y) * dy3
                                      + (pt.Z - a3.Z) * dz3) / ln2 if ln2 else 0.5
                                t2 = t0 + 0.22 if t0 < 0.5 else t0 - 0.22
                                att2 = NXOpen.Point3d(a3.X + t2 * dx3,
                                                      a3.Y + t2 * dy3,
                                                      a3.Z + t2 * dz3)
                            except Exception:
                                pass
                            ld = am.CreateLeaderData()
                            ld.Leader.SetValue(occ(e), view, att2)
                            fcf.Leader.Leaders.Append(ld)
                        fcf.Origin.Origin.SetValue(
                            None, None, NXOpen.Point3d(qx, qy, 0.0))
                    fcf.Commit()
                finally:
                    fcf.Destroy()
            sp.ok = True
            made += 1
        except Exception:
            sp.ok = False
            sp.why = traceback.format_exc().splitlines()[-1]

    # general tolerance note above the title block
    if note_rect is not None:
        try:
            nb = am.CreateDraftingNoteBuilder(NXOpen.Annotations.SimpleDraftingAid.Null)
            try:
                nb.Text.TextBlock.SetText(GDT[metric]["note"])
                nb.Origin.Origin.SetValue(
                    None, None,
                    NXOpen.Point3d(note_rect.x0 + 2 * P, note_rect.y1 - 4 * P, 0.0))
                nb.Commit()
                rep("General tolerance note placed")
            finally:
                nb.Destroy()
        except Exception:
            rep("General tolerance note FAILED: "
                + traceback.format_exc().splitlines()[-1])

    # 6: report
    rep("--- dimension coverage ---")
    for sp in specs:
        if sp.kind == "scheme":
            rep("  INFO %s" % sp.label)
        elif getattr(sp, "skip", False):
            rep("  SKIP %-10s %-24s: %s" % (sp.kind, sp.label, sp.why))
        elif sp.ok:
            rep("  OK   %-10s %-24s -> %s/%s" % (sp.kind, sp.label, sp.view_name, sp.side))
        else:
            rep("  FAIL %-10s %-24s: %s" % (sp.kind, sp.label, sp.why))
    n_ok = sum(1 for sp in specs if sp.ok)
    n_all = sum(1 for sp in specs if sp.kind != "scheme")
    rep("Coverage: %d/%d planned dimensions placed" % (n_ok, n_all))

    wp.Save(NXOpen.BasePart.SaveComponents.TrueValue,
            NXOpen.BasePart.CloseAfterSave.FalseValue)
    say("Saved " + DWG)

    pdfb = wp.PlotManager.CreatePrintPdfbuilder()
    try:
        pdfb.Scale = 1.0
        pdfb.Filename = PDF
        pdfb.SourceBuilder.SetSheets([sheet])
        pdfb.Commit()
        say("PDF exported: " + PDF)
    finally:
        pdfb.Destroy()

    with open(REPORT, "w") as f:
        f.write("\n".join(report_lines) + "\n")
    say("Report: " + REPORT)
    say("DONE")


if __name__ == "__main__":
    main()

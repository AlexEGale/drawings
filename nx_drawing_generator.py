r"""
NX drawing generator — ASME-compliant engineering drawing from a part file.

Runs inside NX via run_journal.exe (headless). Produces a master-model drawing
part (<model>_dwg.prt), a PDF, and a dimension coverage report next to the model.

Usage:  set NXDRAW_MODEL=C:\path\to\model.prt
        "<NX>\NXBIN\run_journal.exe" nx_drawing_generator.py

Pipeline (each stage feeds the next):
  1. analyze   — geometry facts: planar coordinate levels per axis, circles
                 (center/axis/radius), planar-face areas, edge inventory
  2. plan      — every dimension + GD&T control the part needs, assigned to
                 view sides before layout
  3. field     — drawable area + obstacles detected from the template's own
                 curves and annotations (border, title block, notes, symbols)
  4. layout    — scale + view positions solved with reserved bands for the
                 planned dimension rows; iso placed by overlap testing
  5. render    — views, then dimensions/GD&T stacked outward
  6. report    — coverage: what was dimensioned, what was not and why

Standards map — where each rule comes from:

  ASME Y14.1 / Y14.1M (sheet sizes & format)
    - Inch sheets A-F, metric A0-A4; borders, zone grid, title block come from
      the Siemens drawing templates (TEMPLATE_KEEPOUTS marks their regions).
  ASME Y14.3 (orthographic & pictorial views)
    - Third-angle projection: Top view directly above Front, Right view to the
      right of Front, both aligned (solve_layout keeps shared coordinates).
    - Projection symbol on the sheet comes from the template.
    - Pictorial (isometric) view added for clarity; hidden lines omitted in it.
  ASME Y14.2 (line conventions)
    - Hidden lines rendered dashed in orthographic views (view style);
      centerlines generated by NX per the drafting standard.
  ASME Y14.5 (dimensioning & tolerancing; 2009 section refs)
    - para 1.7.1 spacing: first dim line ~10mm from the view, successive rows
      ~6mm min; here 12mm/8mm (reserves(), dim_origin()).
    - Longer dims placed outside shorter ones (row assignment by span) so
      extension lines do not cross dimension lines.
    - Dimension text centered between its arrowheads (dim_origin mid-span).
    - Rectangular-coordinate/baseline dimensioning from the datum planes
      (plan_dimensions: every level dimensioned from the DRF face per axis).
    - Each feature of size fully located from the DRF; equal-value locations
      on different features are still separate dimensions.
    - Hole callouts: diameter symbol via NX hole-style radial dim; "THRU" /
      "<depth> DEEP" appended (words per pre-1994 practice, still allowed).
    - Locations controlled by position tolerances are BASIC dims (para 8.4);
      set via Dimension.ToleranceType.Basic.
    - Datum reference frame (section 4): 3-2-1 scheme — primary datum A =
      largest planar face (seating), B = largest face perpendicular to A,
      C = the remaining direction (pick_datums()).
    - Datum feature qualification: form tolerance (flatness) on the primary
      datum face; orientation (perpendicularity |A, |A|B) on secondary and
      tertiary — frames stacked with their datum feature symbols.
    - Position FCF per hole (section 10): primary datum = the plane
      perpendicular to the hole axis (controls axis squareness), remaining
      datums in precedence order; diameter zone symbol on the tolerance.
    - FCF association: frame grouped directly below the size callout reads as
      applying to that feature of size (para 6.4 placement practice).
    - Leaders must not cross dimension lines; crossing extension lines is
      permitted when unavoidable (datum corner-escape logic in render).
    - General tolerance note ("UNLESS OTHERWISE SPECIFIED ...") covers all
      dims without direct tolerances (para 4.1 / title-block practice).
  Scale selection
    - Preferred ratios only (1:1, 1:2, 1:4, 1:5 ... 2:1, 5:1 ...), largest
      that fits the sheet with all reserved annotation bands (STD_SCALES).

  Default tolerance VALUES (GDT dict) are engineering-plausible placeholders;
  real values are design intent and should come from user configuration.
"""

